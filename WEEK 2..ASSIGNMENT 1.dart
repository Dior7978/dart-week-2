void main() {
  // Integer data type
  int age = 25;

  // Double data type
  double temperature = 23.5;

  // String data type
  String name = 'John Doe';

  // List data type (dynamic list of integers)
  List<int> numbers = [1, 2, 3, 4, 5];

  // Map data type (key-value pairs)
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
    'isEmployed': true,
  };

  // printing the values
  print('Age: $age'); // Output: Age: 25
  print('Temperature: $temperature'); // Output: Temperature: 23.5
  print('Name: $name'); // Output: Name: John Doe
  print('Numbers: $numbers'); // Output: Numbers: [1, 2, 3, 4, 5]
  print('person: $person'); // Output: person: {name: Alice, age: 30, isEmployed: true}
}

